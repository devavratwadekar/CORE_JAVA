
public class If_else_example {

    // The if-else statement extends the if statement by adding an else clause. If the condition is 
    // true, the program executes the code in the if block or else it is false then statement in else 
    // block is executed.
    public static void main(String[] args) {
        int x = 10;

        if (x > 13) {
            System.out.println(x + " is greater than 13");
        } else {
            System.out.println(x + " is less than 13");
        }
    }

}
